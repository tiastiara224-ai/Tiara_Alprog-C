# tipe data dari sebuah nilai

x = 10 # tipe data integer / bilangan bulat
print("nila dari x =", x)

print("tipe dari x =", type (x))

y = 3.5 # tipe dari float / bilangan pecahan
print("nilai dari y =", y)
print("tipe dari y =", type (y))

nama = "ayaa" # tipe dari string / bilangan berupa tulisan
print("nilai dari nama =", nama )
print("tipe dari nama =", type (nama))

kebenaran = True # tipe data bolean
print("nilai dari kebenaran =", kebenaran)
print("tipe dari kebenaran =", type (kebenaran))
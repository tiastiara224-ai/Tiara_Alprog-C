# oprasi aritmatika 
# x / + - 
# dalam pemrograman lamabang x menjadi "*" dan : menjadi "/ dan :"


# penjumlahan lambang (+)
a = 13
b = 9
nilai = a + b
print(a, "+", b, "=", nilai)

# pengurangan lambang (-)
a = 32
b = 10
nilai = a - b
print(a, "-", b, "=", nilai)

# perkalian lambang *
a = 11
b = 2
nilai = a * b
print(a, "*", b, "=", nilai)

# pembagian lambang (/)
a = 44
b = 2
nilai = a / b
print(a, "/", b, "=", nilai)

# perpangkatan lambang (**)
a = 4
b = 2
nilai = a ** b
print(a, "**", b, "=", nilai)

# modulus lambang (%) (sisa bagai)
a = 20
b = 9
nilai = a % b
print(a, "%", b, "=", nilai)

# floor divison lambang (//) (pembulatan)
a = 9
b = 13
nilai = a // b
print(a, "//", b, "=", nilai)


